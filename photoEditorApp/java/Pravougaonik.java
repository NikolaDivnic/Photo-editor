package poop2;

public class Pravougaonik {
	public Pravougaonik(int x, int y, int xx, int yy) {
		super();
		this.x = x;
		this.y = y;
		this.xx = xx;
		this.yy = yy;
	}

	private int x,y,xx,yy;

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public int getXx() {
		return xx;
	}

	public int getYy() {
		return yy;
	}
	
}
